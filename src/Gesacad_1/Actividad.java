package Gesacad_1;

public class Actividad {

    int numactiv;
    String descactiv;

    public Actividad(int numactiv, String descactiv) {
        this.numactiv = numactiv;
        this.descactiv = descactiv;
    }

    public int getNumactiv() {
        return numactiv;
    }

    public void setNumactiv(int numactiv) {
        this.numactiv = numactiv;
    }

    public String getDescactiv() {
        return descactiv;
    }

    public void setDescactiv(String descactiv) {
        this.descactiv = descactiv;
    }
}
